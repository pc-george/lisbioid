    public void setLabelPrecio(){
        float temp = 0;
        for(int i = 0; i < dtm.getRowCount() ; i++){
            temp += (Float)jTable1.getValueAt(i, 4);
        }
        this.total.setText(String.valueOf(temp));
    }
    public DefaultTableModel getModel(){
        return this.dtm;
    }
    public void initTabla(){
        dtm = new DefaultTableModel();
        dtm.addColumn("Cod");
        dtm.addColumn("Titulo");
        dtm.addColumn("Precio");
        dtm.addColumn("Cant.");
        dtm.addColumn("Subtotal");
        
    }
    private Object[] getFila(Libro libro, int cantidad ){
        Object[] datos = new Object[5];
        datos[0] = libro.getId();
        datos[1] = libro.getTitulo();
        datos[2] = libro.getPrecio();
        datos[3] = cantidad;
        datos[4] = (float)(libro.getPrecio() * cantidad);
        
        return datos;
    }
    public void insertarFila(Libro libro, int cantidad){
        this.dtm.addRow(getFila(libro, cantidad));
    }
    public boolean borrarFila(){
        if(jTable1.getSelectedRow() != -1){
            this.dtm.removeRow(jTable1.getSelectedRow());
            return true;
        }
        return false;
    }
    
