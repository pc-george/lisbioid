    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(dtm.getRowCount() == 0)
            JOptionPane.showMessageDialog(this,"Debe ingresar un detalle de remito como mínimo", "Error", JOptionPane.ERROR_MESSAGE);
        else if (dia.getText().compareTo("") == 0 || mes.getText().compareTo("") == 0 || ano.getText().compareTo("") == 0)
            JOptionPane.showMessageDialog(this,"Debe ingresar una fecha", "Error", JOptionPane.ERROR_MESSAGE);
        else if(numero.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(this,"Debe ingresar un numero de Remito", "Error", JOptionPane.ERROR_MESSAGE);
            try{
                Integer.parseInt(numero.getText());
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this,"El número de remito solo debe contener enteros.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try{
                Integer.parseInt(dia.getText());
                Integer.parseInt(mes.getText());
                Integer.parseInt(ano.getText());
            }catch(NumberFormatException er){
                JOptionPane.showMessageDialog(this,"La fecha solo debe contener enteros.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        else if(Remito.existeId(numero.getText())){
            JOptionPane.showMessageDialog(this,"El numero de remito ingresado ya existe en la Base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            
            this.remito = new Remito(numero.getText(), Main.getHora(Integer.parseInt(ano.getText()), Integer.parseInt(mes.getText()), Integer.parseInt(dia.getText())), this.cliente, Float.parseFloat(total.getText()));
            remito.hacerAlta();
            Libro libro;
            EjemplarLibro ejemplar;
            int cantidad;
            for(int i = 0; i < dtm.getRowCount(); i++){
                libro = new Libro((Integer)jTable1.getValueAt(i, 0));
                ejemplar = new EjemplarLibro(libro, libro.getPrecio());
                ejemplar.hacerAlta();
                cantidad = (Integer)jTable1.getValueAt(i, 3);
                DetalleRemito dr = new DetalleRemito(ejemplar, cantidad, this.remito);
                dr.hacerAlta();
            }
            this.padre.insertarFila(this.remito);
            this.dispose();
        }    
    }//GEN-LAST:event_jButton4ActionPerformed

